# Penjumlahan

nilai_A = 30
nilai_B = 20
hasil = nilai_A + nilai_B
print ("jadi hasil penambahan nilai a dan b adalah :" + str(hasil))

# pengurangan

hasil = nilai_A - nilai_B
print ("jadi nilai pengurangan nilai a dan b adalah :" + str(hasil))

# Perkalian
hasil = nilai_A * nilai_B
print("jadi nilai perkalian nilai a dan b adalah :" + str(hasil))
